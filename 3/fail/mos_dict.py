def convert_to_morse_code(word):
    pattern0 = re.compile(r'[ㄱ-ㅎ가-힣]')
    pattern1 = re.compile(r'[-0-9]')
    pattern2 = re.compile(r'[a-zA-Z]')
    if pattern0.match(word):
        wordinfo = 'kor'
    if pattern1.match(word):
        wordinfo = 'num'
    if pattern2.match(word):
        wordinfo = 'eng'
    if word == ' ':
        return ' '

    return mors_dict.get(wordinfo).get(word)    



